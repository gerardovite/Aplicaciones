public class Operaciones {
	
	/**
	 * 
	 * @param n1 Recibe un número entero de la caja de texto N1
	 * @param n2 Recibe un número entero de la caja de texto N1
	 * @return Retorna el resultado de la suma
	 */
	public int suma(int n1, int n2){
		return n1+n2;
	}
	/**
	 * 
	 * @param n1 Recibe un número entero de la caja de texto N1
	 * @param n2 Recibe un número entero de la caja de texto N1
	 * @return Retorna el resultado de la resta
	 */
	public int resta(int n1, int n2){
		return n1-n2;
	}
	/**
	 * 
	 * @param n1 Recibe un número entero de la caja de texto N1
	 * @param n2 Recibe un número entero de la caja de texto N1
	 * @return Retorna el resultado de la multiplicacion
	 */
	public int mult(int n1, int n2){
		return n1*n2;
	}

	/**
	 * 
	 * @param n1 Recibe un número entero de la caja de texto N1
	 * @param n2 Recibe un número entero de la caja de texto N1
	 * @return Retorna el resultado de la division
	 */
	public int div(int n1, int n2){
		return n1/n2;
	}
}
